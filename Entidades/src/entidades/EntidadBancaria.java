package entidades;

public class EntidadBancaria{
    private int codigo_E;
    private String nombre_E;
    private String direccion_E;
    private String ruc_E;

    public int getCodigo_E() {
        return codigo_E;
    }

    public void setCodigo_E(int codigo_E) {
        this.codigo_E = codigo_E;
    }

    public String getNombre_E() {
        return nombre_E;
    }

    public void setNombre_E(String nombre_E) {
        this.nombre_E = nombre_E;
    }

    public String getDireccion_E() {
        return direccion_E;
    }

    public void setDireccion_E(String direccion_E) {
        this.direccion_E = direccion_E;
    }

    public String getRuc_E() {
        return ruc_E;
    }

    public void setRuc_E(String ruc_E) {
        this.ruc_E = ruc_E;
    }
}
